/* 
 * clear value of dropdown box  
 * @param {String || Object]} selectObj object dropdown box
 */ 
function removeOptions(selectObj) 
{ 
    if (typeof selectObj != 'object') 
    { 
        selectObj = document.getElementById(selectObj); 
    } 
 
    // count of options 
    var len = selectObj.options.length; 
 
    for (var i=0; i < len; i++) 
    { 
        // remove current option 
        selectObj.options[0] = null; 
    } 
} 
 
/* 
 * send the value of option selected to another dropdown box
 * 
 * @param {String || Object]} selectObj object dropdown box 
 * @param {Array} optionList format of option：[{txt:'Beijing', val:'010'}, {txt:'Shanghai', val:'020'}]
 * @param {String} firstOption value of the first option, eg,"Please Select" 
 * @param {String} selected default value 
 */ 
function setSelectOption(selectObj, optionList, firstOption, selected) 
{ 
    if (typeof selectObj != 'object') 
    { 
        selectObj = document.getElementById(selectObj); 
    } 
 
    // clear option 
    removeOptions(selectObj); 
 
    // count of option 
    var start = 0; 
     
    // add the first option 
    if (firstOption) 
    { 
        selectObj.options[0] = new Option(firstOption, ''); 
 
        // count options from 1 
        start ++; 
    } 
 
    var len = optionList.length; 
 
    for (var i=0; i < len; i++) 
    { 
        // set option 
        selectObj.options[start] = new Option(optionList[i].txt, optionList[i].val); 
 
        // option selected 
        if(selected == optionList[i].val) 
        { 
            selectObj.options[start].selected = true; 
        } 
 
        // add 1 
        start ++; 
    } 
 
} 